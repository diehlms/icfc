	<h1 class="my-4 text-3xl font-bold">Family Agreements</h1>

	<div class="rounded border border-blue-500 bg-blue-100 px-4 py-3 text-blue-700">
		<h2 class="font-semibold">March 30, 2012</h2>
		<p>
			Club Policy Regarding Family Agreements relating to ICFC expense sharing and membership
			transfer
		</p>
	</div>

	<div class="text-container mt-4">
		<p>
			The Club is aware that over the years quite a few ICFC member families have entered into
			private agreements among family members to address and govern the sharing of ICFC-related
			expenses (including cottage ownership, cottage maintenance, and dues) among family members. In
			some instances, family agreements also address the subjects of membership status and transfers
			of membership among family members.
		</p>
		<p>
			The Club recognizes that such agreements can be useful means of resolving uncertainty
			regarding the issues that they address. The Club believes that it is appropriate for family
			members to enter into such agreements when they desire to do so. The Club will honor outcomes
			dictated by such agreements where the outcomes are consistent with existing Club By-Laws.
		</p>
		<p>
			The Club will take no position regarding the enforceability of private family agreements. It
			will offer no advice regarding the terms of such agreements, and will have no role in the
			enforcement or interpretation of such agreements. In matters of Club affairs, the ICFC By-Laws
			as drafted take precedence over anything to the contrary in a private family agreement. (By
			way of example, it would be contrary to the By-Laws and the By-Laws would take precedence if a
			family agreement said that siblings A and B were both deemed to be corporate members, even
			though only one of them had been elected a corporate member and paid corporate dues.)
		</p>
	</div>
</div>
