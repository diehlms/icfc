		<p>
			To the fullest extent that the laws of the Commonwealth of Pennsylvania, as in effect on
			January 27, 1987, or as thereafter amended, permit elimination or limitation of the liability
			of Directors, no Directors of the Club shall be personally liable for monetary damages as such
			for any action taken, or any failure to take any action, as a Director.
		</p>
		<p>Section 1– Right to Indemnification</p>
		<p>
			Each person who at any time is or shall have been a Director, officer or employee of the Club,
			and his or her heirs, executors and administrators, shall be indemnified by the Club in
			accordance with and to the full extent permitted by the laws of the Commonwealth of
			Pennsylvania. The foregoing right of indemnification shall constitute a contract between the
			Club and each of its Directors, officers and employees and shall not be deemed exclusive of
			other rights to which any Director, officer, employee, agent or other person may be entitled
			in any capacity as a matter of law or under any by-law, agreement, vote of the Directors, or
			otherwise.
		</p>
		<p>Section 2 – Right to Advancement of Expenses</p>
		<p>
			Every person entitled to be indemnified hereunder shall be entitled to have his or her
			expenses in defending any action, or in initiating and pursuing any action for indemnity or
			advancement of expenses, paid in advance by the Club prior to final disposition of such
			action; provided that the Club receives a written undertaking by or on behalf of such person
			to repay the amount advanced if it should ultimately be determined that such person is not
			entitled to be indemnified for such expenses.
		</p>
		<p>Section 3 – Right to Purchase Insurance</p>
		<p>
			The Club may purchase and maintain insurance to protect itself and any person eligible to be
			indemnified hereunder against any expense or liability asserted or incurred by such person in
			connection with any action, whether or not the Club would have the power to indemnify such
			person against such expense or liability by law or under the provisions of this Article.
		</p>
		<p>Section 4 – Applicability of Article</p>
		<p>
			This Article shall apply to every action other than an action filed prior to January 27, 1987,
			except that it shall not apply to the extent that Pennsylvania law does not permit its
			application to any breach of performance of duty or any failure of performance of duty by any
			Director or indemnitee occurring prior to January 27, 1987.
